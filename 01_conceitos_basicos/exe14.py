''' Escreva um programa que converta uma temperatura digitada em °C e Converta para °F'''
print('------------------------------ Programa Conversor de Temperatura °C/°F ------------------------------')
# variavel para guardar o valor do celsius: 
c = float(input('->> digite a tempertaura em Celsius: '))

# variavel para converter e guardar o valor de fahrenheit:
f = (c*1.8)+32

print(f'A temperatura de {c}°C para Farenheint\n.....Convertendo !!....\n->> Temperatura em Farenheit: {f}°F\n-----------------------------fim---------------------------');